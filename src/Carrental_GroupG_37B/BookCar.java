    private void DateFirstFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_DateFirstFocusGained
        if (DateFirst.getDate() == null) {
            DateFirst.setDate(null);
            DateFirst.setForeground(java.awt.Color.BLACK);
        }
    }//GEN-LAST:event_DateFirstFocusGained

    private void DateFirstFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_DateFirstFocusLost
        if (DateFirst.getDate() == null) {
            DateFirst.setDate(new java.util.Date());
            DateFirst.setForeground(java.awt.Color.GRAY);
        }
    }//GEN-LAST:event_DateFirstFocusLost

