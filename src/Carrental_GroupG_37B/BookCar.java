    private void PickupFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_PickupFocusGained
        if (Pickup.getText().trim().equals("Pickup Location")) {
            Pickup.setText("");
            Pickup.setForeground(java.awt.Color.BLACK);
        }
    }//GEN-LAST:event_PickupFocusGained

    private void PickupFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_PickupFocusLost
         if (Pickup.getText().trim().isEmpty()) {
            Pickup.setText("Pickup Location");
            Pickup.setForeground(java.awt.Color.GRAY);
        }
    }//GEN-LAST:event_PickupFocusLost

    private void DropFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_DropFocusGained
        if (Drop.getText().trim().equals("Enter Drop Location")) {
            Drop.setText("");
            Drop.setForeground(java.awt.Color.BLACK);
        }
    }//GEN-LAST:event_DropFocusGained

    private void DropFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_DropFocusLost
        if (Drop.getText().trim().isEmpty()) {
            Drop.setText("Enter Drop Location");
            Drop.setForeground(java.awt.Color.GRAY);
        }
    }//GEN-LAST:event_DropFocusLost

